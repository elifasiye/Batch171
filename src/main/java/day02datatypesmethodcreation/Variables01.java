package day02datatypesmethodcreation;

public class Variables01 {


    public static void main(String[] args) {
        //Ornek1: Sehir ismi için bir variable olusturun ve once "Ankara" sonra "İzmir"degeri atayıpekrana yazdirin.

        String cityName = "Ankara" ;
        System.out.println(cityName);

        cityName = "Izmir";
        System.out.println(cityName);


        //Ornek 2: sehir plaka kodu icin  bir variable olusturun once 34 sonra da degeri atayip console a yazdiriniz
        byte plakaKodu=34;
        System.out.println(plakaKodu);

        plakaKodu=35;
        System.out.println(plakaKodu);

// Basarili mi ? sorusu icin bir variable olusturun ve once "false" sonada "true" degeri atayıp ekrana yazdırın.

        boolean isSuccessful = false;
        System.out.println(isSuccessful);


        isSuccessful=true;
        System.out.println(isSuccessful);










    }


}

